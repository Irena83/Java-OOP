public class DataTypes5 {
    public static void main(String[] args) {

        int stranica = 5;
        int poluprecnik = 5;
        double pi = 3.14;

        int povrsina_kvadrata = stranica * stranica;
        double povrsina_kruga = poluprecnik * poluprecnik * pi;

        System.out.println("Povrsina kvadrata je " + povrsina_kvadrata);
        System.out.println("Povrsina kruga je " + povrsina_kruga);
    }
}
